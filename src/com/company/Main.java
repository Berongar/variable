package com.company;

public class Main {

    public static void main(String[] args) {
        int var1; //объявляем переменную 1
        int var2; //обьявляем переменную 2

        var1 = 1024; //присваиваем значение переменной

        System.out.println("Переменная var1 содержит: " + var1); //Выводим значение первой переменной

        var2 = var1/2; // выполняем арифметическое действие

        System.out.print("Переменная var2 содержит var1/2: ");
        System.out.println(var2);
    }
}
